package basic;

public class JavaSwitch {
	public static void main(String[] args) {
		
		// Switch ~ case
		// key를 입력받고 해당 Key 동일한 case를 실행
		
		// key: 동물
		switch (key) {
		case "사자":
			System.out.println("어흥");
			break;
		case "양":
			System.out.println("메~~");
			break;
		case "원숭이":
			System.out.println("우끼끼");
			break;
		default:
			break;
		} 
	}
}
